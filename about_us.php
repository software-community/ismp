              Indian Institute of Technology, Ropar (IIT Ropar) is one of the new IITs(established in 2008) set up by the Ministry of Human Resource Development (MHRD), Government of India. The Government of Punjab has allotted 501 acres of land on the banks of the river Sutlej to IIT Ropar. The foundation stone of the Institute was laid on 24th February 2009. The institute enables students to gain exposure to recent trends in their chosen domains of study and practical experience through a wide variety of activities that the institute facilitates in its own campus and arranges for in collaboration with industry and other institutes. This campus is a self-contained township catering to all the needs of faculty, staff and students. Arrangements have been made for classes, laboratories, hostels and faculty accommodation.
